async function fetchEmployeeData() {
  const response = await fetch("http://127.0.0.1:5000/api/users", {
    cache: "no-cache",
  });

  if (!response.ok) {
    throw new Error("Failed to fetch employee data");
  }

  const result = await response.json();


  // Check if the data is directly an array or is nested
  if (Array.isArray(result)) {
    return result;
  } else if (result.data && Array.isArray(result.data)) {
    return result.data;
  } else if (result.users && Array.isArray(result.users)) {
    return result.users;
  } else if (result.employees && Array.isArray(result.employees)) {
    return result.employees;
  }

  // If we can't find an array, throw a more descriptive error
  throw new Error(
    "API response format is not as expected: " + JSON.stringify(result)
  );
}

